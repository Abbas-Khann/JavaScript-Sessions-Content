// program to find the sum of all numbers upto 100 from 1

// setting the initial sum 0
let sum = 0 
let i = 1 // the iterator starts from 1
while (i <= 100) { // iteration upto 100
    sum+=i // iterator value added to the sum
    i++ // iterator increased by one
}
console.log(`The sum of all the number from 1 to 100 is: ${sum}`) // logging the sum
